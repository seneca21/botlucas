// services/bot.service.js

const { Telegraf, Markup } = require('telegraf');
const { createCharge, checkPaymentStatus } = require('./qr.service');
const path = require('path');
const fs = require('fs');
const ConfigService = require('./config.service');
const { Sequelize } = require('sequelize');
const UserModel = require('../models/User');

/**
 * Fun√ß√£o auxiliar para converter boolean para texto
 */
function booleanParaTexto(value, verdadeiro, falso) {
  return value ? verdadeiro : falso;
}

// Carrega o config.json
const config = ConfigService.loadConfig();
// Carrega as configs de banco (DATABASE_URL, etc)
const dbConfig = ConfigService.getDbConfig();

// Inicializa Sequelize
const sequelize = new Sequelize(dbConfig.connectionString, {
  dialect: dbConfig.dialect,
  dialectOptions: dbConfig.dialectOptions,
  logging: false, // Desativa logs do Sequelize
});

// Puxa o model User (voc√™ j√° incluiu botName no model!)
const User = UserModel(sequelize);

// Sincroniza o banco (cria/altera tabelas conforme o model)
sequelize.sync({ alter: true })
  .then(() => {
    console.log('‚úÖ Modelos sincronizados e tabelas alteradas conforme necess√°rio.');
  })
  .catch((err) => {
    console.error('‚ùå Erro ao sincronizar os modelos:', err);
  });

// Array de bots e sess√µes de usu√°rios em mem√≥ria
const bots = [];
const userSessions = {};

/**
 * Inicializa cada bot configurado no config.json
 */
function initializeBot(botConfig) {
  const bot = new Telegraf(botConfig.token);
  console.log(`üöÄ Bot ${botConfig.name} em execu√ß√£o.`);

  /**
   * Registra ou atualiza o usu√°rio no banco
   */
  async function registerUser(ctx) {
    try {
      const telegramId = ctx.from.id.toString();

      // Tenta encontrar ou criar
      const [user, created] = await User.findOrCreate({
        where: { telegramId },
        defaults: {
          username: ctx.from.username,
          firstName: ctx.from.first_name,
          lastName: ctx.from.last_name,
          languageCode: ctx.from.language_code,
          isBot: ctx.from.is_bot,
          lastInteraction: new Date(),
          remarketingSent: false,
          hasPurchased: false,

          // Importante: salva o nome do bot
          botName: botConfig.name,
        },
      });

      // Info de remarketing e compra para logs
      const statusRemarketing = booleanParaTexto(user.remarketingSent, 'Enviado', 'N√£o Enviado');
      const statusCompra = booleanParaTexto(user.hasPurchased, 'Comprado', 'Sem Compra');

      if (created) {
        console.log(`‚úÖ Usu√°rio registrado: ${telegramId}, Remarketing: ${statusRemarketing}, Compra: ${statusCompra}`);
      } else {
        user.lastInteraction = new Date();
        // Se quiser garantir sempre o botName atualizado, fa√ßa:
        user.botName = botConfig.name;
        await user.save();
        console.log(`üîÑ Usu√°rio atualizado: ${telegramId}, Remarketing: ${statusRemarketing}, Compra: ${statusCompra}`);
      }

      // Dispara uma mensagem de remarketing se ele n√£o comprou depois de X minutos
      const notPurchasedInterval = botConfig.remarketing.intervals.not_purchased_minutes || 5;
      setTimeout(async () => {
        try {
          const currentUser = await User.findOne({ where: { telegramId } });
          if (currentUser && !currentUser.hasPurchased && !currentUser.remarketingSent) {
            await sendRemarketingMessage(currentUser, 'not_purchased');
            currentUser.remarketingSent = true;
            await currentUser.save();
            console.log(`‚úÖ Mensagem de remarketing enviada para ${telegramId}`);
          }
        } catch (err) {
          console.error(`‚ùå Erro ao enviar mensagem de remarketing para ${telegramId}:`, err);
        }
      }, notPurchasedInterval * 60 * 1000);

    } catch (error) {
      console.error('‚ùå Erro ao registrar usu√°rio:', error);
    }
  }

  /**
   * Envia mensagens de remarketing conforme a condi√ß√£o
   */
  async function sendRemarketingMessage(user, condition) {
    try {
      // Localiza a config da mensagem
      const messageConfig = botConfig.remarketing.messages.find(msg => msg.condition === condition);
      if (!messageConfig) {
        console.error(`‚ùå Mensagem de remarketing n√£o encontrada para a condi√ß√£o: ${condition}`);
        return;
      }

      const videoPath = path.resolve(__dirname, `../src/videos/${messageConfig.video}`);
      if (!fs.existsSync(videoPath)) {
        console.error(`‚ùå Arquivo de v√≠deo n√£o encontrado: ${videoPath}`);
        return;
      }

      // Confere se todo bot√£o tem "name"
      for (const button of messageConfig.buttons) {
        if (!button.name) {
          console.error(`‚ùå Um dos bot√µes de remarketing est√° sem 'name'.`);
          return;
        }
      }

      // Cria o markup dos bot√µes
      const remarketingButtonMarkup = messageConfig.buttons.map((button) =>
        Markup.button.callback(button.name, `remarketing_select_plan_${button.value}`)
      );

      // Envia a mensagem de remarketing com v√≠deo
      await bot.telegram.sendVideo(user.telegramId, { source: videoPath }, {
        caption: messageConfig.text,
        parse_mode: 'HTML',
        ...Markup.inlineKeyboard(remarketingButtonMarkup, { columns: 1 }),
      });
    } catch (error) {
      console.error(`‚ùå Erro ao enviar mensagem de remarketing:`, error);
    }
  }

  /**
   * Tratamento global de erros do Telegraf
   */
  bot.catch((err, ctx) => {
    console.error(`‚ùå Erro no bot:`, err);
    if (err.response && err.response.error_code === 403) {
      console.warn(`üö´ Bot bloqueado pelo usu√°rio ${ctx.chat.id}.`);
    } else {
      ctx.reply('‚ö†Ô∏è Ocorreu um erro inesperado. Tente novamente mais tarde.');
    }
  });

  /**
   * A√ß√£o "remarketing_select_plan_X" - user clica em um bot√£o de remarketing
   */
  bot.action(/^remarketing_select_plan_(\d+(\.\d+)?)$/, async (ctx) => {
    const chatId = ctx.chat.id;
    const planValue = parseFloat(ctx.match[1]);

    // Procura esse plano tanto no array principal quanto no remarketing
    const mainPlan = botConfig.buttons.find(btn => btn.value === planValue);
    const remarketingPlan = botConfig.remarketing.messages
      .flatMap(msg => msg.buttons)
      .find(btn => btn.value === planValue);

    const plan = mainPlan || remarketingPlan;
    if (!plan) {
      console.error(`‚ùå Plano com valor ${planValue} n√£o encontrado.`);
      await ctx.reply('‚ö†Ô∏è Plano n√£o encontrado. Tente novamente.');
      await ctx.answerCbQuery();
      return;
    }

    // Salva no user o plano escolhido (opcional)
    const user = await User.findOne({ where: { telegramId: chatId.toString() } });
    if (user) {
      user.lastInteraction = new Date();
      user.botName = botConfig.name; // caso queria manter atualizado
      await user.save();
    }

    console.log(`‚úÖ Plano ${plan.name} (${plan.value} R$) enviado com sucesso ‚úÖ`);

    try {
      // Gera a cobran√ßa
      const chargeData = {
        value: plan.value * 100,
        webhook_url: null,
      };
      const chargeResult = await createCharge(chargeData);
      const chargeId = chargeResult.id;
      const emv = chargeResult.qr_code;

      // Salva na sess√£o
      if (!userSessions[chatId]) userSessions[chatId] = {};
      userSessions[chatId].chargeId = chargeId;
      userSessions[chatId].selectedPlan = plan;

      // Envia o PIX
      await ctx.reply(
        `üìÑ C√≥digo PIX gerado com sucesso!\n\`\`\`\n${emv}\n\`\`\``,
        { parse_mode: 'Markdown' }
      );
      // Bot√£o de verificar pagamento
      await ctx.reply(
        '‚ö†Ô∏è Ap√≥s pagamento, clique em "Verificar Pagamento".',
        Markup.inlineKeyboard([
          Markup.button.callback('üîç Verificar Pagamento', `check_payment_${chargeId}`),
        ])
      );
    } catch (error) {
      console.error('‚ùå Erro ao criar a cobran√ßa via remarketing:', error);
      if (error.response && error.response.error_code === 403) {
        console.warn(`üö´ Bot bloqueado pelo usu√°rio ${ctx.chat.id}.`);
        delete userSessions[chatId];
      } else {
        await ctx.reply('‚ö†Ô∏è Erro ao criar a cobran√ßa. Tente mais tarde.');
      }
    }

    await ctx.answerCbQuery();
  });

  /**
   * /start
   */
  bot.start(async (ctx) => {
    try {
      console.info('üì© Comando /start recebido');
      await registerUser(ctx);

      const videoPath = path.resolve(__dirname, `../src/videos/${botConfig.video}`);
      if (!fs.existsSync(videoPath)) {
        console.error(`‚ùå V√≠deo n√£o encontrado: ${videoPath}`);
        await ctx.reply('‚ö†Ô∏è Erro ao carregar o v√≠deo.');
        return;
      }

      // Bot√µes de planos
      const buttonMarkup = botConfig.buttons.map((button, index) =>
        Markup.button.callback(button.name, `select_plan_${index}`)
      );

      // Envia o v√≠deo inicial
      await ctx.replyWithVideo(
        { source: videoPath },
        {
          caption: botConfig.description,
          parse_mode: 'HTML',
          ...Markup.inlineKeyboard(buttonMarkup, { columns: 1 }),
        }
      );

      console.log(`üé• V√≠deo e bot√µes de plano enviados para ${ctx.chat.id}`);
    } catch (error) {
      console.error('‚ùå Erro no /start:', error);
      if (error.response && error.response.error_code === 403) {
        console.warn(`üö´ Bot bloqueado pelo usu√°rio ${ctx.chat.id}.`);
      } else {
        await ctx.reply('‚ö†Ô∏è Erro ao processar /start.');
      }
    }
  });

  /**
   * A√ß√£o "select_plan_X"
   */
  bot.action(/^select_plan_(\d+)$/, async (ctx) => {
    const chatId = ctx.chat.id;
    const buttonIndex = parseInt(ctx.match[1], 10);
    const buttonConfig = botConfig.buttons[buttonIndex];

    if (!buttonConfig) {
      console.error(`‚ùå Plano √≠ndice ${buttonIndex} n√£o encontrado.`);
      await ctx.reply('‚ö†Ô∏è Plano n√£o encontrado.');
      await ctx.answerCbQuery();
      return;
    }

    // Atualiza user com a √∫ltima intera√ß√£o e o botName, se quiser
    const user = await User.findOne({ where: { telegramId: chatId.toString() } });
    if (user) {
      user.lastInteraction = new Date();
      user.botName = botConfig.name;
      await user.save();
    }

    console.log(`‚úÖ Plano ${buttonConfig.name} (${buttonConfig.value} R$) enviado com sucesso ‚úÖ`);

    try {
      // Cria cobran√ßa
      const chargeData = {
        value: buttonConfig.value * 100,
        webhook_url: null,
      };
      const chargeResult = await createCharge(chargeData);
      const chargeId = chargeResult.id;
      const emv = chargeResult.qr_code;

      if (!userSessions[chatId]) userSessions[chatId] = {};
      userSessions[chatId].chargeId = chargeId;
      userSessions[chatId].selectedPlan = buttonConfig;

      await ctx.reply(
        `üìÑ C√≥digo PIX gerado!\n\`\`\`\n${emv}\n\`\`\``,
        { parse_mode: 'Markdown' }
      );
      await ctx.reply(
        '‚ö†Ô∏è Ap√≥s pagar, clique em "Verificar Pagamento".',
        Markup.inlineKeyboard([
          Markup.button.callback('üîç Verificar Pagamento', `check_payment_${chargeId}`),
        ])
      );
    } catch (error) {
      console.error('‚ùå Erro ao criar cobran√ßa:', error);
      if (error.response && error.response.error_code === 403) {
        console.warn(`üö´ Bot bloqueado por ${ctx.chat.id}.`);
        delete userSessions[chatId];
      } else {
        await ctx.reply('‚ö†Ô∏è Erro ao criar cobran√ßa.');
      }
    }

    await ctx.answerCbQuery();
  });

  /**
   * /status_pagamento
   */
  bot.command('status_pagamento', async (ctx) => {
    const chatId = ctx.chat.id;
    const session = userSessions[chatId];

    if (!session || !session.chargeId) {
      await ctx.reply('‚ö†Ô∏è Nenhuma cobran√ßa em andamento.');
      return;
    }

    try {
      console.info('üîç Verificando pagamento...');
      const paymentStatus = await checkPaymentStatus(session.chargeId);

      if (paymentStatus.status === 'paid') {
        await ctx.reply('üéâ Pagamento confirmado!');
        const user = await User.findOne({ where: { telegramId: chatId.toString() } });
        if (user) {
          user.hasPurchased = true;
          await user.save();
          const statusCompra = booleanParaTexto(user.hasPurchased, 'Comprado', 'Sem Compra');
          console.log(`‚úÖ Usu√°rio ${chatId} marcado como ${statusCompra}. Plano: ${user.planName} R$${user.planValue}`);

          // Dispara upsell
          const purchasedInterval = botConfig.remarketing.intervals.purchased_seconds || 30;
          setTimeout(async () => {
            try {
              const currentUser = await User.findOne({ where: { telegramId: chatId.toString() } });
              if (currentUser && currentUser.hasPurchased) {
                await sendRemarketingMessage(currentUser, 'purchased');
                console.log(`‚úÖ Upsell enviado para ${chatId}`);
              }
            } catch (err) {
              console.error(`‚ùå Erro upsell ${chatId}:`, err);
            }
          }, purchasedInterval * 1000);
        }

        // Link do produto
        const selectedPlan = session.selectedPlan;
        if (selectedPlan && selectedPlan.link) {
          await ctx.reply(`üéâ Produto: [Acessar](${selectedPlan.link})`, { parse_mode: 'Markdown' });
        } else {
          await ctx.reply('‚ö†Ô∏è Link do produto n√£o encontrado.');
        }

        delete userSessions[chatId];
      } else if (paymentStatus.status === 'expired') {
        await ctx.reply('‚ùå A cobran√ßa expirou.');
        delete userSessions[chatId];
      } else {
        await ctx.reply('‚è≥ Aguardando pagamento...');
      }
    } catch (error) {
      console.error('‚ùå Erro ao verificar pagamento:', error);
      if (error.response && error.response.error_code === 403) {
        console.warn(`üö´ Bot bloqueado por ${ctx.chat.id}.`);
        delete userSessions[chatId];
      } else {
        await ctx.reply('‚ö†Ô∏è Erro ao verificar pagamento.');
      }
    }
  });

  /**
   * A√ß√£o check_payment_X
   */
  bot.action(/check_payment_(.+)/, async (ctx) => {
    const chatId = ctx.chat.id;
    const chargeId = ctx.match[1];
    const session = userSessions[chatId];

    if (!session || session.chargeId !== chargeId) {
      await ctx.reply('‚ö†Ô∏è Cobran√ßa n√£o corresponde.');
      return;
    }

    try {
      console.info('üîç Verificando pagamento...');
      const paymentStatus = await checkPaymentStatus(chargeId);

      if (paymentStatus.status === 'paid') {
        await ctx.reply('üéâ Pagamento confirmado!');
        const user = await User.findOne({ where: { telegramId: chatId.toString() } });
        if (user) {
          user.hasPurchased = true;
          await user.save();
          const statusCompra = booleanParaTexto(user.hasPurchased, 'Comprado', 'Sem Compra');
          console.log(`‚úÖ Usu√°rio ${chatId} ${statusCompra}.`);

          // Envia upsell
          const purchasedInterval = botConfig.remarketing.intervals.purchased_seconds || 30;
          setTimeout(async () => {
            try {
              const currentUser = await User.findOne({ where: { telegramId: chatId.toString() } });
              if (currentUser && currentUser.hasPurchased) {
                await sendRemarketingMessage(currentUser, 'purchased');
                console.log(`‚úÖ Upsell enviado ${chatId}`);
              }
            } catch (err) {
              console.error(`‚ùå Erro upsell ${chatId}:`, err);
            }
          }, purchasedInterval * 1000);

          // Link do produto
          const selectedPlan = session.selectedPlan;
          if (selectedPlan && selectedPlan.link) {
            await ctx.reply(`üéâ Produto: [Acessar](${selectedPlan.link})`, { parse_mode: 'Markdown' });
          } else {
            await ctx.reply('‚ö†Ô∏è Link do produto n√£o encontrado.');
          }
          delete userSessions[chatId];
        }
      } else if (paymentStatus.status === 'expired') {
        await ctx.reply('‚ùå Cobran√ßa expirada.');
        delete userSessions[chatId];
      } else {
        await ctx.reply('‚è≥ Pagamento pendente.');
      }
    } catch (error) {
      console.error('‚ùå Erro status pagamento:', error);
      if (error.response && error.response.error_code === 403) {
        console.warn(`üö´ Bot bloqueado ${ctx.chat.id}`);
        delete userSessions[chatId];
      } else {
        await ctx.reply('‚ö†Ô∏è Erro ao verificar pagamento.');
      }
    }

    await ctx.answerCbQuery();
  });

  // Lan√ßa o bot
  bot.launch()
    .then(() => {
      console.info(`üöÄ Bot ${botConfig.name} iniciado com sucesso.`);
    })
    .catch((error) => {
      console.error(`üî• Erro ao iniciar ${botConfig.name}:`, error);
    });

  // Permite encerramento gracioso
  process.once('SIGINT', () => bot.stop('SIGINT'));
  process.once('SIGTERM', () => bot.stop('SIGTERM'));

  // Guarda no array
  bots.push(bot);
}

// Inicia cada bot configurado
for (const botConfig of config.bots) {
  initializeBot(botConfig);
}

// Exporta se precisar
module.exports = bots;
